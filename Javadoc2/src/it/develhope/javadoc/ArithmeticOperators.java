package it.develhope.javadoc;

/**
 * this class contains your arithmetic methods
 */
public class ArithmeticOperators {
    /**
     * this method use an additive operator
     * @param num1 enter your desired number
     * @param num2 enter your desired number
     * @return return the sum
     */
    public int sum(int num1, int num2){
        return num1 + num2;
    }

    /**
     * this method use a substraction operator
     * @param num1 enter your desired number
     * @param num2 enter your desired number
     * @return return the subtraction
     */
    public int sub(int num1, int num2){
        return num1 - num2;
    }

    /**
     * this method use a multiplication operator
     * @param num1 enter your desired number
     * @param num2 enter your desired number
     * @return return the multiplication
     */
    public int mul(int num1, int num2){
        return num1 * num2;
    }

    /**
     * this method use a division operator
     * @param num1 enter your desired number
     * @param num2 enter your desired number
     * @return return the division
     */
    public int div(int num1, int num2){
        return num1 / num2;
    }

   
}
